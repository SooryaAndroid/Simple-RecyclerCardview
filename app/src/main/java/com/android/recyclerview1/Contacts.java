public class Contacts {
    String names;
    String emails;
    String phnos;

    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }

    public String getEmails() {
        return emails;
    }

    public void setEmails(String emails) {
        this.emails = emails;
    }

    public String getPhnos() {
        return phnos;
    }

    public void setPhnos(String phnos) {
        this.phnos = phnos;
    }
}
